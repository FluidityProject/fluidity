from math import cos, sin


def nu_T(X):
    return (
        0.800 * cos(0.3183098861837907 * X[0] * X[1])
        + 0.400 * sin(X[0])
        + 0.300 * sin(X[1])
        + 2.00
    )


def c(X):
    return cos(X[0]) + 0.600 * sin(X[1]) + 3.00


def forcing_c(X):
    return (
        (
            1.60 * cos(0.3183098861837907 * X[0] * X[1])
            + 0.800 * sin(X[0])
            + 0.600 * sin(X[1])
            + 5.00
        )
        * cos(X[0])
        - 0.600
        * (
            -0.509295817894065 * X[0] * sin(0.3183098861837907 * X[0] * X[1])
            + 0.600 * cos(X[1])
        )
        * cos(X[1])
        + (
            -0.509295817894065 * X[1] * sin(0.3183098861837907 * X[0] * X[1])
            + 0.800 * cos(X[0])
        )
        * sin(X[0])
        + 0.600
        * (
            1.60 * cos(0.3183098861837907 * X[0] * X[1])
            + 0.800 * sin(X[0])
            + 0.600 * sin(X[1])
            + 5.00
        )
        * sin(X[1])
    )
